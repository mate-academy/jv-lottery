package core.basesyntax;

public class Ball {
    private String color;
    private int number;
    @Override
    public String toString() {
        return super.toString();
    }
    public void setNumber(int number) {
        this.number = number;
    }
    public void setColor(String color) {
        this.color = color;
    }
    public String getColor() {
        return color;
    }
    public int getNumber() {
        return number;
    }
}
